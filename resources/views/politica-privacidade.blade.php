    <h1>Política de Privacidade</h1>
    <p><strong>Última atualização:</strong> 07/07/2024</p>
    <p>A sua privacidade é importante para nós. Esta política de privacidade explica como coletamos, usamos, compartilhamos e protegemos suas informações ao usar o Ajoci, nosso aplicativo gerenciador financeiro.</p>

    <h2>1. Informações que Coletamos</h2>
    <p><strong>a. Informações Pessoais:</strong> Quando você se cadastra no Ajoci, podemos coletar informações pessoais, como seu nome, endereço de e-mail e informações de contato.</p>
    <p><strong>b. Informações de Uso:</strong> Coletamos informações sobre como você usa o Ajoci, incluindo dados sobre o dispositivo, sistema operacional, navegador e endereço IP.</p>

    <h2>2. Como Usamos Suas Informações</h2>
    <p><strong>a. Para Fornecer e Melhorar Nossos Serviços:</strong> Usamos suas informações para operar, manter e melhorar o Ajoci, personalizando sua experiência e fornecendo suporte ao cliente.</p>
    <p><strong>b. Comunicações:</strong> Podemos usar suas informações de contato para enviar atualizações, notificações e informações importantes sobre o Ajoci. Você pode optar por não receber essas comunicações a qualquer momento.</p>
    <p><strong>c. Segurança:</strong> Utilizamos suas informações para monitorar e melhorar a segurança do Ajoci, prevenir fraudes e proteger seus dados.</p>

    <h2>3. Compartilhamento de Informações</h2>
    <p><strong>a. Com Terceiros:</strong> Não compartilhamos suas informações pessoais com terceiros, exceto conforme necessário para operar o Ajoci (por exemplo, provedores de serviços de hospedagem) ou conforme exigido por lei.</p>
    <p><strong>b. Requisitos Legais:</strong> Podemos divulgar suas informações se acreditarmos que é necessário para cumprir uma obrigação legal, proteger nossos direitos, ou proteger a segurança de nossos usuários.</p>

    <h2>4. Segurança das Informações</h2>
    <p>Implementamos medidas de segurança para proteger suas informações contra acesso, alteração, divulgação ou destruição não autorizada. No entanto, nenhuma transmissão de dados pela internet é completamente segura, e não podemos garantir a segurança absoluta de suas informações.</p>

    <h2>5. Retenção de Dados</h2>
    <p>Retemos suas informações pelo tempo necessário para fornecer nossos serviços ou conforme exigido por lei. Quando suas informações não forem mais necessárias, as excluiremos de maneira segura.</p>

    <h2>6. Seus Direitos</h2>
    <p>Você tem o direito de acessar, corrigir, atualizar ou excluir suas informações pessoais. Para exercer esses direitos, entre em contato conosco pelo e-mail <a href="mailto:contato@ajoci.com">contato@ajoci.com</a>.</p>

    <h2>7. Alterações nesta Política de Privacidade</h2>
    <p>Podemos atualizar esta política de privacidade periodicamente. Notificaremos você sobre quaisquer alterações publicando a nova política de privacidade no Ajoci. Recomendamos que você revise esta política regularmente.</p>

    <h2>8. Contato</h2>
    <p>Se você tiver alguma dúvida ou preocupação sobre esta política de privacidade, entre em contato conosco pelo e-mail <a href="mailto:contato@ajoci.com">contato@ajoci.com</a>.</p>
